# 2.Расставить скобки так, чтобы значение выражений не поменялось.
a = 17 / 2 * 3 + 2
print(a)  # 27.5
new_a = (17 / 2) * 3 + 2
print(new_a)  # 27.5

b = 2 + 17 / 2 * 3
print(a)  # 27.5
new_b = 2 + (17 / 2) * 3
print(new_b)  # 27.5

c = 19 % 4 + 15 / 2 * 3
print(c)  # 25.5
new_c = 19 % 4 + (15 / 2) * 3
print(new_c)  # 25.5

d = (15 + 6) - 10 * 4
print(d)  # -19
new_d = (15 + 6) - 10 * 4
